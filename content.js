function getLangFromUrl() {
  const params = new URLSearchParams(window.location.search);
  const lang = params.get("lang");
  return lang === "zh" || lang === "en" ? lang : null;
}

function detectDefaultLang() {
  const browserLang = (navigator.language || "en").toLowerCase();
  return browserLang.startsWith("zh") ? "zh" : "en";
}

function resolveLang() {
  const stored = localStorage.getItem("lang");
  if (stored === "zh" || stored === "en") return stored;

  const fromUrl = getLangFromUrl();
  if (fromUrl) return fromUrl;

  return detectDefaultLang();
}

function renderList(id, items) {
  const ul = document.getElementById(id);
  ul.innerHTML = "";
  items.forEach((item) => {
    const li = document.createElement("li");
    li.textContent = item;
    ul.appendChild(li);
  });
}

function renderContacts(items) {
  const ul = document.getElementById("contact-list");
  ul.innerHTML = "";
  items.forEach((item) => {
    const li = document.createElement("li");
    li.innerHTML = `${item.label}: <a href="${item.href}">${item.text}</a>`;
    ul.appendChild(li);
  });
}

function setLanguage(lang) {
  const data = content[lang] || content.en;
  document.documentElement.lang = lang;
  document.title = data.pageTitle;

  document.getElementById("name").textContent = data.name;
  document.getElementById("tagline").textContent = data.tagline;
  document.getElementById("about-title").textContent = data.aboutTitle;
  document.getElementById("about-text").textContent = data.aboutText;
  document.getElementById("interests-title").textContent = data.interestsTitle;
  document.getElementById("publications-title").textContent = data.publicationsTitle;
  document.getElementById("projects-title").textContent = data.projectsTitle;
  document.getElementById("education-title").textContent = data.educationTitle;
  document.getElementById("contact-title").textContent = data.contactTitle;
  document.getElementById("contact-message").textContent = data.contactMessage;
  document.getElementById("footer-text").textContent = data.footer;

  renderList("interests-list", data.interests);
  renderList("publications-list", data.publications);
  renderList("projects-list", data.projects);
  renderList("education-list", data.education);
  renderContacts(data.contact);

  document.getElementById("lang-en").classList.toggle("active", lang === "en");
  document.getElementById("lang-zh").classList.toggle("active", lang === "zh");
}

function initLanguage() {
  const initialLang = resolveLang();
  setLanguage(initialLang);

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      const lang = button.getAttribute("data-lang");
      localStorage.setItem("lang", lang);
      setLanguage(lang);
    });
  });
}

initLanguage();
